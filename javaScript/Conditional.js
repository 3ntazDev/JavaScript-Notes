//==================
// if statement
//==================

// if statement تفحص شرط معين وتنفذ كود معين بناءً على نتيجة الشرط
// إذا الشرط صحيح (true) تنفذ الكود الموجود بين الأقواس المعقوفة {}
// إذا الشرط خطأ (false) ما ينفذ الكود

let year = 2015;

if (year == 2015) {
  console.log("صح! السنة 2015");   // ينطبع لأن الشرط صحيح
}

// لو كان في أكثر من سطر نستخدم الأقواس { }
// حتى لو سطر واحد يُفضل استخدامها لتحسين القراءة

// ==========================
// جافاسكربت تحول الشرط لقيمة منطقية (boolean)
// ==========================

// إذا الشرط يعطي قيم "Falsy" مثل: 0، "" (نص فاضي)، null، undefined، NaN
// لن ينفذ الكود داخل if

if (0) {
  console.log("هذا ما راح ينطبع");  // 0 يعتبر خطأ (Falsy)
}

if ("0") {
  console.log("هذا بينطبع");        // نص "0" ليس فارغ، يعتبر صحيح (Truthy)
}

// ==========================
// استخدام else - لو الشرط false نفذ هذا الكود
// ==========================

if (year == 2015) {
  console.log("السنة 2015");
} else {
  console.log("السنة مش 2015");
}

// ==========================
// else if - فحص شروط متعددة
// ==========================

if (year < 2015) {
  console.log("قبل 2015");
} else if (year > 2015) {
  console.log("بعد 2015");
} else {
  console.log("بالضبط 2015");
}

// ==========================
// المشغل الشرطي ?: (الشرط الثلاثي - ternary operator)
// ==========================

// طريقة مختصرة لكتابة if..else
// الصيغة: شرط ? قيمة_إذا_صحيح : قيمة_إذا_خطأ

let age = 20;

let accessAllowed = (age > 18) ? true : false;

console.log(accessAllowed);  // true لأن 20 أكبر من 18

// ممكن نستغني عن ?: إذا الشرط نفسه يعيد true أو false

let access = age > 18;  // يعيد true أو false مباشرة

console.log(access);

// ==========================
// استخدام ?: مع شروط متعددة (تداخل ternary)
// ==========================

let age2 = 25;

let message = (age2 < 3) ? 'مرحبا طفل!' :
              (age2 < 18) ? 'أهلاً!' :
              (age2 < 100) ? 'تحية!' :
              'عمر غير معتاد';

console.log(message);

// نفس المثال باستخدام if..else

if (age2 < 3) {
  message = 'مرحبا طفل!';
} else if (age2 < 18) {
  message = 'أهلاً!';
} else if (age2 < 100) {
  message = 'تحية!';
} else {
  message = 'عمر غير معتاد';
}

console.log(message);

// ==========================
// نصيحة: لا تستخدم ?: لتنفيذ أكواد (كود جانبي) فقط للعودة بقيمة
// استخدم if للقيام بأكثر من مجرد إرجاع قيمة

let company = "Netscape";

if (company == "Netscape") {
  console.log("صحيح!");
} else {
  console.log("خطأ!");
}

// لو كتبتها بالشرط الثلاثي؟ أقل وضوح وأقل قراءة

(company == "Netscape") ? console.log("صحيح!") : console.log("خطأ!");

// ==========================
// ملخص:
// - if..else تستعمل لتنفيذ أكواد بناءً على شرط
// - ?: تستعمل لإرجاع قيمة بناءً على شرط، مفيدة للاختصارات
// - استخدم {} مع if دائما لتحسين القراءة
// - خذ بالك من قيم truthy و falsy عند التحقق من الشروط