// التفاعل مع المستخدم - Interaction: alert, prompt, confirm

// الجافاسكربت توفر ثلاث دوال بسيطة نتفاعل فيها مع المستخدم:
// alert → عرض رسالة
// prompt → طلب إدخال نص
// confirm → تأكيد (نعم أو لا)

// ----------------------
// 🟢 alert
// ----------------------
// تعرض رسالة للمستخدم وتنتظر ضغط "OK"
alert("مرحبًا بك في موقعنا!");

// ✨ ملاحظة:
// النافذة هذي اسمها "نافذة مشروطة - Modal"
// يعني توقف كل شيء في الصفحة لين المستخدم يتفاعل معها

// ----------------------
// 🟢 prompt
// ----------------------
// تطلب من المستخدم يدخل نص، ونقدر نحفظه في متغير
let userName = prompt("ما اسمك؟", "فهد");

// إذا ضغط OK → ترجع النص المدخل
// إذا ضغط Cancel → ترجع null

// نقدر نستخدم القيمة
alert(`أهلًا بك يا ${userName}!`);

// ✨ ملاحظة للمتصفحات القديمة زي Internet Explorer:
// لازم تحط القيمة الافتراضية (حتى لو فاضية) عشان ما تطلع "undefined"
let test = prompt("اختبار", ""); // ✅ أفضل طريقة

// ----------------------
// 🟢 confirm
// ----------------------
// تسأل المستخدم سؤال بنعم أو لا
let isAdmin = confirm("هل أنت المدير؟");

// ترجع true إذا ضغط OK، و false إذا ضغط Cancel
alert(`إجابة المستخدم: ${isAdmin}`);

// ----------------------
// 🟠 خلاصة:
// ----------------------
// alert → يعرض رسالة فقط
// prompt → يطلب إدخال نص، يرجع نص أو null
// confirm → يطلب تأكيد، يرجع true أو false

// كلها نوافذ مشروطة (Modal) توقف الكود لين المستخدم يرد

// ✨ ملاحظات مهمة:
// - مكان النافذة يتحدد من المتصفح (ما نقدر نتحكم فيه)
// - شكلها يختلف من متصفح لثاني (ما نقدر نعدله)
// - بس استخدامها سريع وسهل ومناسب للتجارب البسيطة

